--
-- Создать процедуру `get_random_name`
--
CREATE DEFINER = 'root'@'localhost'
PROCEDURE get_random_name ()
BEGIN
  SELECT
    COUNT(*)
  FROM firstname INTO @cnt_first;
  SELECT
    COUNT(*)
  FROM secondname INTO @cnt_second;

  SET @r = RAND();
  SELECT
    firstname
  FROM firstname
  WHERE id >= @r * @cnt_first
  LIMIT 1 INTO @first;
  SELECT
    secondname
  FROM secondname
  WHERE id >= @r * @cnt_second
  LIMIT 1 INTO @second;

  SELECT
    @first,
    @second;

END
$$

