DELIMITER $$

--
-- Создать процедуру `new_launch`
--
CREATE DEFINER = 'root'@'localhost'
PROCEDURE new_launch (IN elevcount int, IN floorcount int, IN passcount int, IN capacity int, OUT launchid int)
BEGIN
  INSERT INTO launches (lstart, elevcount, floorcount, passcount, capacity)
    VALUES (NOW(), elevcount, floorcount, passcount, capacity);
  SET launchid = LAST_INSERT_ID();
END
$$

--
-- Создать процедуру `close_launch`
--
CREATE DEFINER = 'root'@'localhost'
PROCEDURE close_launch (IN launchid int)
BEGIN
  UPDATE launches l
  SET l.lend = NOW()
  WHERE l.id = launchid;
END
$$

